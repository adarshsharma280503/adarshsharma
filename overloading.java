class overloading
{
void adarsh()
{
System.out.println(" this is my address");
}
void adarsh(int i)
{
System.out.println(i + " " + " this is my address");
}
void adarsh(int i, String a)
{
System.out.println(i + " " + a + " " + " this is my address");
}
void adarsh(int i, String a, String b)
{
System.out.println(i + " " + a + " " + b +" " + " this is my address");
}
}
